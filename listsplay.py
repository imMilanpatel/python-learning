# Playing with nested list

fruits = ["Strawberries", "Nectarines", "Apples", "Grapes", "Peaches", "Cherries", "Pears"]
vegetables = ["Spinach", "Kale", "Tomatoes", "Celery", "Potatoes"]

# PlAY
dirty_dozen = [fruits, vegetables]

# Print the two nested lists individually
print(dirty_dozen[0])
print(dirty_dozen[1])

# Print from second nested list the last element
print(dirty_dozen[1][-1])


# In a nested list the first element index is 0 and so forth for the remaining inc by 1