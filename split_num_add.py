# Split the number and add it

# Get the input from the user
two_digit_number = input("Type a two digit number: ")

# Subscript the strings
first_digit = int(two_digit_number[0])
second_digit = int(two_digit_number[1])

# Adding the two digits number
add_digits = first_digit + second_digit

# Print the number
print(add_digits)


